    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     int maxClie = Integer.parseInt(this.clientesTxt.getText());
    int maxLlamada = Integer.parseInt(this.llamadasTxt.getText());
    Date inicio = new Date();
    
    // Limpiamos las tablas antes de agregar nuevos datos
    modeloCli.setRowCount(0);
    modeloLlamada.setRowCount(0);
    
    // Creamos clientes y llamadas
    for (int i = 0; i < maxClie; i++) {
        cliente nuevoCliente = new cliente("C" + i, "123456789-0", "Dept" + i);
        nuevoCliente.Illamada = new ArrayList<>();
        
        for (int j = 0; j < maxLlamada; j++) {
            Date fechaHora = new Date();
            int duracion = (int) (Math.random() * 60); // DuraciÃ³n aleatoria en minutos
            String numeroOrigen = "010" + (int)(Math.random() * 1000);
            String numeroDestino = "020" + (int)(Math.random() * 1000);
            double costoLlamada = Math.round(Math.random() * 10); // Costo aleatorio
            
            llamada nuevaLlamada = new llamada(fechaHora, duracion, numeroOrigen, numeroDestino, costoLlamada);
            nuevoCliente.Illamada.add(nuevaLlamada);
            
            // Agregamos la llamada a la tabla
            String[] registroL = {
                formatoFecha.format(nuevaLlamada.FechaHora),
                Integer.toString(nuevaLlamada.Duracion),
                nuevaLlamada.NumeroOrigen,
                nuevaLlamada.NumeroDestino,
                Double.toString(nuevaLlamada.CostoLlamada)
            };
            modeloLlamada.addRow(registroL);
        }
        
        // Agregamos el cliente a la tabla
        String[] registroC = {nuevoCliente.Codigo, nuevoCliente.Dui, nuevoCliente.Departamento};
        modeloCli.addRow(registroC);
        
        // Agregamos el cliente a la lista
        lClie.add(nuevoCliente);
    }
    
    Date fin = new Date();
    long duracion = fin.getTime() - inicio.getTime();
    this.jLabel4.setText(Long.toString(duracion) + " ms");
    this.jLabel5.setText(Long.toString(duracion / 1000) + " s");
     this.jButton1.setEnabled(false);
    }//GEN-LAST:event_jButton1ActionPerformed

